console.log(Math);

//? abs 
// convert negative number to positive 
console.log(Math.abs(-30));

//? round 
// roundoff to nearest number
console.log(Math.round(20.8));

//? floor
// roundoff to lowest nearest number
console.log(Math.floor(12.8));

//? ceil()
// roundoff to highest nearest number
console.log(Math.ceil(20.4));

//? pow()
// it will return the power value of number 
console.log(2**3);
console.log(Math.pow(2,3));

//? sqrt()
// it will return the square root value of number
console.log(Math.sqrt(4));

//? cbrt()
// it will return the cube root value of a number 
console.log(Math.cbrt(8));

//? min()
// it will return the minimum no of numbers
console.log(Math.min(10,20,-6,8));

//? max()
// it will return the minimum no of numbers
console.log(Math.min(10,20,-6,8));

//? random
// it will return number between 0 to 1
console.log(Math.random());

//? trunc()
// it will remove the decimal values.
console.log(Math.trunc(50.855));

